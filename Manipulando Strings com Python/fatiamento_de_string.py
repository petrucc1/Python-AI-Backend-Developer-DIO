# Retorna substrings, partes de uma string original, de acordo com a posição inicial e final que você especificar.
nome = 'João da Silva Pereira'

nome[0]
# 'J'

nome[:4]
# 'João'

nome[10:]
# 'Silva Pereira'

nome[10:15]
# 'Silva'

nome[10:15:2]
# 'Sia'

nome[:]
# 'João da Silva Pereira'

nome[::-1]
# 'ariereP avliS ad oãoJ'