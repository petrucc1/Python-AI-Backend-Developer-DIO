# A Lista é uma estrutura mutável, ou seja, é possível alterar seus valores após a criação.
# A Tupla é uma estrutura imutável, ou seja, não é possível alterar seus valores após a criação.
# Uma Tupla pode ser criada de duas formas: colocando valores separados por vírgula utilizando parênteses ou a função tuple().

frutas = ('banana', 'laranja', 'abacaxi', 'melancia',) # Tupla criada com parênteses, deixar a vírgula no final é uma boa prática.

letras = tuple('Python') # Tupla criada com a função tuple().

numeros = tuple([1, 2, 3, 4, 5]) # Tupla criada com a função tuple().

pais = ('Brasil',) # Tupla criada com parênteses, deixar a vírgula no final é uma boa prática.
